/**
 * Get the most appropriate image for a topic
 * @param {string} topic - The search topic/keyword
 * @param {string} fallback - Fallback image URL
 * @returns {string} Image URL
 */
export function getTopicImage(topic, fallback = TOPIC_IMAGES.general) {
  if (!topic) return fallback
  
  const normalizedTopic = topic.toLowerCase().trim()
  
  // Exact match
  if (TOPIC_IMAGES[normalizedTopic]) {
    return TOPIC_IMAGES[normalizedTopic]
  }
  
  // Partial match - check if any keyword is contained in the topic
  for (const [keyword, imageUrl] of Object.entries(TOPIC_IMAGES)) {
    if (normalizedTopic.includes(keyword) || keyword.includes(normalizedTopic)) {
      return imageUrl
    }
  }
  
  return fallback
}
